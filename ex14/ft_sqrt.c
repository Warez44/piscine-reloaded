int		ft_sqrt(int nb)
{
	int sq;

	sq = 1;
	if (nb <= 0)
		return (0);
	while (sq * sq < nb)
	{
		sq++;
	}
	if (nb / sq == sq)
		return (sq);
	else
		return (0);
}
